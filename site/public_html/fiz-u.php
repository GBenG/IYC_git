	<p align="center"> <strong> ТОВ «Юридична компанія« ІнЮрКомпані »<br> пропонує свої послуги фізичним особам в таких галузях права, як: </strong> </p>

<strong> <a name="1"> </a> Договірне право </strong> <br>

Безумовно, галузь договірних правовідносин є найпоширенішою в Україну. А тому, і кількість самих різноманітних проблем, з якими зустрічається людина у своєму повсякденному житті, є також досить об'ємною. Враховуючи це, кожному окремому клієнту юристи компанія «ІнЮрКомпані» індивідуально визначають правову підгалузь, яка регулює ті чи інші оскаржувані, невизнані або порушені права клієнта і підбирають найбільш конкретні і дієві способи і шляхи захисту Ваших прав та інтересів, в тому числі:
<ul>
<li> юридичні консультації з питань укладання всіх видів цивільно-правових договорів між фізичними особами, (в тому числі договорів на пайову участь у будівництві житла); </li>
<li> підготовка проектів договорів; </li>
<li> ведення переговорів щодо можливих змін чи уточнень договорів, що укладатимуться, з урахуванням Ваших інтересів; </li>
<li> супровід угод Вами; </li>
<li> врегулювання спорів, що виникають при неналежному виконанні або невиконанні, зміні та розірванні договорів; </li>
<li> відшкодування морального і матеріального збитку; </li>
<li> стягнення заборгованості; </li>
<li> врегулювання спорів з наступним поданням Ваших інтересів в усіх державних та місцевих органах, в судах всіх інстанцій. </li>
</Ul>

<strong> <a name="2"> </a> Спадкове право </strong> <br>
Чинне цивільне законодавство України в повному обсязі регулює спадкові відносини, встановлює види спадкування, коло осіб, які можуть бути спадкоємцями та інше. Тому спірні ситуації, пов'язані зі спадкуванням та використанням майна, мають місце в життєвій практиці людей.
З метою вирішення таких ситуації, юристи компанії «ІнЮрКомпані» надають кваліфіковану допомогу з питань:
<ul>
<li> консультування з питань оформлення спадщини, продовження термінів для прийняття спадщини тощо; </li>
<li> юридична допомога при складанні заповітів, спадкових договорів; </li>
<li> оскарження заповітів, визнання недійсними свідоцтв про право на спадщину; </li>
<li> представництво Ваших інтересів при оформленні спадщини в органах нотаріату, державної влади та ін.; </li>
<li> представництво і юридичний супровід вирішення спорів, що випливають із спадкування, в судах всіх інстанцій. </li>
</Ul>

<strong> <a name="3"> </a> Сімейне право </strong> <br>
Сімейне життя є дуже різноманітним і, при, непередбачуваним. Відносини подружжя є дуже відповідальним. Це полягає насамперед у змісті загального майна, гідне виховання дітей кожним із подружжя і т.д.. Але, як це не сумно констатувати, сімейні суперечки в нашій країні є досить частими і важкими з моральної точки зору для кожної із сторін спору. Юристи компанії «ІнЮрКомпані» готові надати Вам якісну та професійну допомогу у вирішенні сімейних спорів, враховуючи, насамперед, як юридичні аспекти, так і моральні засади суспільства, зокрема:
<ul>
<li> консультації та представництво інтересів клієнта з питань: </li>
<ul>
<li> укладання та розірвання шлюбу; </li>
<li> майнових прав подружжя та прав на утримання (аліменти); </li>
<li> поділу спільно нажитого подружжям майна; </li>
<li> реєстрації народження дітей, встановлення батьківства тощо; </li>
<li> реалізації батьками прав на участь у вихованні дітей; </li>
</Ul>
<li> консультації з усиновлення, встановлення опіки та піклування; </li>
<li> юридичний супровід процедури позбавлення батьківських прав; </li>
<li> врегулювання сімейних суперечок. </li>
</Ul>

<strong> <a name="4"> </a> Житлове право </strong> <br>
Житлові спори є досить конфліктними і багатогранними правовідносинами, які пов'язані з вирішенням мабуть самого актуального в наш час питання. Ми допоможемо Вам у вирішенні житлових спорів будь-якої складності:
<ul>
<li> юридичний аналіз Вашої конкретної ситуації, складання оптимальної стратегії дій, яка максимально враховує Ваші інтереси; </li>
<li> підготовка позовних заяв, скарг, клопотань та запитів та інших процесуальних документів до суду, органів влади та місцевого самоврядування; </li>
<li> представлення інтересів клієнтів у всіх державних органах і органах місцевого самоврядування, в судах всіх інстанцій з питань о), встановлення порядку використання ним визнання ордера і свідоцтва про право власності недійсним; </li>
<li> консультації щодо надання, оренди, приватизації житла. </li>
<li> питання іпотечного кредитування і застави. </li>
</Ul>

<strong> <a name="5"> </a> Трудове право </strong> <br>
Конституція України встановлює, що кожна людина і громадянин мають право на працю. Ми впевнені, що кожен з тих, хто реалізує це право, зустрічався з аспектами та нюансами трудового правовідносини, починаючи з підписання трудового договору і подальшої взаємодії працівника і роботодавця. На жаль, їхні інтереси часто не збігаються. Отже тому, сьогодні трудові спори стають все поширеними. Фахівці нашої компанії готові допомогти Вам і надати кваліфіковану допомогу у вирішенні питань:
<ul>
<li> представлення інтересів працівника при оскарженні дій роботодавця, вирішенні трудових спорів з роботодавцем в комісії по трудових спорах, суді, в тому числі: </li>
<ul>
<li> врегулювання спорів, що виникають при прийомі на роботу, переведення на іншу роботу, невиплату заробітної плати; </li>
<li> оскарження рішень роботодавця про притягнення працівника до матеріальної, дисциплінарної відповідальності; </li>
<li> ащіту інтересів працівника, на якого незаконно накладено дисциплінарне стягнення (в т.ч. і звільнення); </li>
</Ul>
<li> представлення інтересів роботодавця в процесі проведення перевірок контролюючими органами з питань використання праці найманих працівників; </li>
<li> юридичні консультації по застосуванню трудового законодавства з будь-яким іншим питанням, які виникають у Клієнта. </li>
</Ul>

<strong> <a name="6"> </a> Адміністративне право </strong> <br>
Юридична компанія «ІнЮрКомпані» пропонує послуги для фізичних осіб, в тому числі, фізичних осіб-підприємців, пов'язані з супроводом різних адміністративних процедур, які є досить поширеними в процесі ведення бізнесу.
Юристи нашої компанії допоможуть у вирішенні таких першочергових питань, як:
<ul>
<li> вивчення та аналіз матеріалів адміністративних справ, а також дій та (або) документів органів влади або місцевого самоврядування тощо; </li>
<li> складання заперечень на акти перевірок податкових та інших контролюючих органів; </li>
<li> оскарження неправомірних дій та документів органів ДАІ; </li>
<li> підготовка і подача необхідних процесуальних документів до суду відповідної інстанції; </li>
<li> захист Ваших інтересів в адміністративних судах та інше. </li>
</Ul>

<strong> <a name="7"> </a> Податкове право </strong> <br>
У зв'язку з прийняттям нового Податкового кодексу України, у тих, хто є платниками податків, виникає все більше і більше питань, пов'язаних із законодавчим регулюванням податкових правовідносин.
Фахівці Юридичної компанії «ІнЮрКомпані» допоможуть Вам і нададуть кваліфіковану правову допомогу з наступних питань у сфері оподаткування:
<ul>
<li> консультування з питань оподаткування; </li>
<li> юридичний аналіз сплати податків клієнтами на предмет його відповідності вимогам податкового законодавства; </li>
<li> оскарження дій посадових осіб органів Державної податкової адміністрації; </li>
<li> оскарження рішень органів ДПА; </li>
<li> юридична підтримка при проведенні податкових перевірок, а також спорів з податковими та іншими контролюючими органами; в тому числі представництво інтересів Клієнтів у судах тощо. </li>
</Ul>

<strong> <a name="8"> </a> Якщо Ви стали учасником ДТП </strong> <br>
Сьогодні, мабуть, уже нікого не здивуєш такою кількістю автомобілів, що є на наших дорогах. Маючи автомобіль, його власник повинен не забувати про те, що перш, волею долі, його засіб пересування, на жаль, може стати учасником ДТП.
Юристи компанії «ІнЮрКомпані» мають чималу практику з вирішенням спорів, пов'язаних з настанням ДТП. Це дає нам можливість надати клієнтам юридичну допомогу з питань:
<ul>
<li> юридичної підтримки та допомоги у відшкодуванні шкоди, заподіяної внаслідок ДТП; </li>
<li> юридичного супроводу процедури стягнення шкоди, заподіяної внаслідок ДТП; </li>
<li> правової допомоги в процедурі стягнення сум страхового відшкодування з недобросовісних страхових компаній; </li>
<li> оскарження незаконних рішень про визнання провини в ДТП; </li>
<li> представлення інтересів потерпілого в ДТП. </li>
</Ul>

<strong> <a name="9"> </a> Представництво інтересів в судах </strong> <br>
Безумовно, головним напрямком нашої роботи є представництво наших Клієнтів у судах всіх без винятку інстанцій. Юридична компанія «ІнЮрКомпані» в особі її співробітників достатньо успішно і впевнено здійснює представництво в суді з розгляду цивільних, сімейних, адміністративних, господарських та інших видів спорів. Робота, пов'язана з представництвом наших Клієнтів в суді, складається з:
<ul>
