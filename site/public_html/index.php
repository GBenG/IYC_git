	<p align="center"><strong>Приветствуем Вас на официальном сайте «Юридической компании «ИнЮрКомпани»</strong></p>
	          <strong>ООО «Юридическая компания «ИнЮрКомпани»</strong> специализирующаяся на предоставлении юридических услуг юридическим лицам и физическим лицам (гражданам) в наиболее актуальных областях гражданского, налогового, корпоративного, семейного, трудового, жилищного и земельного права. Мы предоставляем квалифицированные <strong>юридические услуги</strong> на территории г. Луганска и Луганской области, в особых случаях возможны выезды специалистов в более отдаленные города.<br><br>
	
    &nbsp;&nbsp;&nbsp;&nbsp;Данный сайт является официальным интернет-представительством "ЮК «ИнЮрКомпани», основная задача которого - ответить на наиболее часто задаваемые вопросы посетителей и предоставить им максимум объективной информации о том, как оказание юридических услуг поможет разрешить ту или иную проблему, какова стоимость юридических услуг и сроки их исполнения.  Если клиенту не нужны какие-то юридические услуги и обслуживание, мы не станем данные услуги навязывать, ограничившись только тем набором юридических услуг который необходим клиенту.<br><br>

	 &nbsp;&nbsp;&nbsp;&nbsp; Ни для кого не секрет, что каждый день все мы, без исключения, общаемся с разными людьми, покупаем и продаем товары, предоставляем услуги или ими пользуемся. Другими словами, мы все являемся участниками общественных отношений различных сфер жизни. В нашей стране, как и во многих других, существуют множество видов правоотношений, в частности такие, как: гражданские, трудовые, финансовые, жилищные, земельные, семейные, хозяйственные, корпоративные, налоговые, административные и многие другие. А если существуют отношения, то всегда существуют и вопросы и проблемы, которые из них вытекают. Безусловно, каждый из нас, хоть раз встречался с этим довольно неприятным явлением. Поэтому, считаем, что эта информация будет полезной как для тех, кто только что встретился с непредсказуемыми ситуациями, так и для тех, кто, к сожалению, уже имеет проблемные вопросы, но с их решением возникают некоторые трудности, которые имеют юридически-правовой характер.<br><br>

	&nbsp;&nbsp;&nbsp;&nbsp;Наша компания оказывает юридические услуги, которые связаны не только с решением конкретных вопросов и проблем граждан и юридических лиц в той или иной сфере общественной жизни. Наша работа заключается, прежде всего, в детальном анализе проблемы и разработке способов его преодоления соответствии с действующим законодательством. Юристы нашей компании тщательно анализируют все Ваши устные или письменные объяснения, в том числе, документы, сопровождающие то или иное конкретную ситуацию и предоставляют содержательные выводы относительно дальнейших действий, которые бы содержали кратчайший путь достижения определенных целей с юридической точки зрения.<br><br>

	&nbsp;&nbsp;&nbsp;&nbsp;Сфера деятельности нашей компании, а также приобретенный практический опыт дают уверенность в том, что мы можем быть полезны практически каждому активному человеку и представителю бизнеса.<br><br>
 
	&nbsp;&nbsp;&nbsp;&nbsp;На сегодняшний день, наша юридическая компания может предложить Вам сотрудничество по всем основным юридическим направлениям.<br><br>
	&nbsp;&nbsp;&nbsp;&nbsp;<strong>Представление интересов граждан в суде</strong> - представляет интересы граждан в судах всех инстанций. Юридическая помощь по ведению дела в суде может предоставляться как комплексно "под ключ", так и в форме отдельно взятых работ (например, только подготовка искового заявления или кассационной жалобы). Мы помогаем при рассмотрении дел по спорам о защите прав потребителей,  трудовые споры о восстановлении на работе, взыскании задолженности по заработной плате, семейные споры о разделе имущества супругов, расторжении брака, взыскании страхового возмещения со страховых компаний, возмещение ущерба от ДТП, залития, причинения вреда жизни и здоровью, окажем <strong>юридические услуги по недвижимости</strong>, признанию права собственности на недвижимое имущество (квартиру, гараж, нежилые помещения) через суд, возьмем на себя полное юридическое сопровождение по таким делам как наследственные споры, жилищные споры, земельные споры и прочее.<br>
	<br>

	&nbsp;&nbsp;&nbsp;&nbsp; <strong>Юридическое обслуживание и сопровождение организаций</strong>  - это комплекс юридических услуг, предоставляемых организациям по льготным ценам и направленный на решение всех текущих юридических проблем, возникающих на конкретном предприятии.  Данный вид услуг намного экономичнее штатного юриста и эффективнее по соотношению: затраты/квалификация. Объем услуг, стоимость и принцип взаимодействия формируются по согласованию с заказчиком и в строгой зависимости от потребностей его компании. Юридическая фирма - это больше, чем просто опытный и квалифицированный специалист, поскольку она несет ответственность за качество своих услуг. <br><br>


    &nbsp;&nbsp;&nbsp;&nbsp; <strong>Юридические консультации</strong> - предоставляет письменные и устные юридические консультации гражданам и организациям по различным правовым вопросам. Чтобы узнать, могут ли Вам помочь в Вашем вопросе и записаться на юридическую консультацию, достаточно позвонить нашим специалистам.  Юридическая консультация - первый шаг на пути решения проблемы. Для того, чтобы юридическая консультация прошла максимально эффективно, рекомендуем кратко изложить суть проблемы нашим специалистам при первом звонке, когда вы записываетесь на консультацию.

